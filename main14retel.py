import re

# валидация мобильного номера в международном формате
"""
+7(912)15-16-896, 8(912)15-16-896
+79121516896, 89121516896
+7(912)151-68-96, 8(912)151-68-96
+7912-151-6896, 87912-151-6896
"""

number="+7(912)15-14-123"
# pattern = re.compile(r'(\+7|8).*?(\d{2,3}).*?(\d{2,3}).*?(\d{2}).*?(\d{2})')
pattern = re.compile(r'\+7|8)[\W\s-]*?(\d{2,3})[\W\s-]*?(\d{2,3})[\W\s-]*?(\d{2})[\W\s-]*?(\d{2,3}')

if re.match(pattern, number):
    print("ДА")
else:
    print("НЕТ")